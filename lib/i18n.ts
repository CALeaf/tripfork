export function translateLevel(level: "Low" | "Medium" | "High", locale: Locale) {
  if (locale === "en") return level;
  return level === "Low" ? "低" : level === "High" ? "高" : "中";
}

export function translateComplexity(level: "Low" | "Medium" | "High", locale: Locale) {
  if (locale === "en") return level === "Low" ? "Simple" : level === "High" ? "Complex" : "Moderate";
  return level === "Low" ? "简单" : level === "High" ? "复杂" : "适中";
}

export function translateKind(kind: "book" | "cancel" | "check" | "decide", locale: Locale) {
  if (locale === "en") return kind;
  return { book: "预订", cancel: "取消", check: "核实", decide: "决策" }[kind];
}
